package br.com.ctup.beersomm.modelo.entidade;

public class Palativo  {
	
 String alcool;
 String acidez;
 String textura;
 String corpo;
 String retrogosto;
 String adstringencia;
 String drinkability;
 int pontuacaoPalativa;
 
 
 
public String getAlcool() {
	return alcool;
}
public void setAlcool(String alcool) {
	this.alcool = alcool;
}
public String getAdstringencia() {
	return adstringencia;
}
public void setAdstringencia(String adstringencia) {
	this.adstringencia = adstringencia;
}
public String getDrinkability() {
	return drinkability;
}
public void setDrinkability(String drinkability) {
	this.drinkability = drinkability;
}
public int getPontuacaoPalativa() {
	return pontuacaoPalativa;
}
public void setPontuacaoPalativa(int pontuacaoPalativa) {
	this.pontuacaoPalativa = pontuacaoPalativa;
}
public String getAcidez() {
	return acidez;
}
public void setAcidez(String acidez) {
	this.acidez = acidez;
}
public String getTextura() {
	return textura;
}
public void setTextura(String textura) {
	this.textura = textura;
}
public String getCorpo() {
	return corpo;
}
public void setCorpo(String corpo) {
	this.corpo = corpo;
}
public String getRetrogosto() {
	return retrogosto;
}
public void setRetrogosto(String retrogosto) {
	this.retrogosto = retrogosto;
}


}
